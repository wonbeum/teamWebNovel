package com.example.model;

public class novelInfoTO {
	private String novel_title;
	private String novel_genre;
	private String novel_writer;
	private String novel_img;
	private String novel_content;
	private String novel_series;
	private String novel_kakao;
	private String novel_ridi;
	public String getNovel_title() {
		return novel_title;
	}
	public void setNovel_title(String novel_title) {
		this.novel_title = novel_title;
	}
	public String getNovel_genre() {
		return novel_genre;
	}
	public void setNovel_genre(String novel_genre) {
		this.novel_genre = novel_genre;
	}
	public String getNovel_writer() {
		return novel_writer;
	}
	public void setNovel_writer(String novel_writer) {
		this.novel_writer = novel_writer;
	}
	public String getNovel_img() {
		return novel_img;
	}
	public void setNovel_img(String novel_img) {
		this.novel_img = novel_img;
	}
	public String getNovel_content() {
		return novel_content;
	}
	public void setNovel_content(String novel_content) {
		this.novel_content = novel_content;
	}
	public String getNovel_series() {
		return novel_series;
	}
	public void setNovel_series(String novel_series) {
		this.novel_series = novel_series;
	}
	public String getNovel_kakao() {
		return novel_kakao;
	}
	public void setNovel_kakao(String novel_kakao) {
		this.novel_kakao = novel_kakao;
	}
	public String getNovel_ridi() {
		return novel_ridi;
	}
	public void setNovel_ridi(String novel_ridi) {
		this.novel_ridi = novel_ridi;
	}
	
}
